number = input('введите число от 1 до 12 ')
if number == 1 or number == 2 or number == 12:
    print('winter')
elif number == 3 or number == 4 or number == 5:
    print('spring')
elif number == 6 or number == 7 or number == 8:
    print('summer')
elif number == 9 or number == 10 or number == 11:
    print('autumn')
else:
    print('error')